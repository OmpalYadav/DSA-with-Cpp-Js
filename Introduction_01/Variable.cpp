#include <iostream>
using namespace std;

// Variables are containers used to store data or values in memory locations.
// We use variables to store information that can be used and modified throughout a program.
// To declare a variable in C++, you specify its data type followed by its name. For example:
// int age; // declares an integer variable named 'age'
// Variables are used to make programs flexible and readable by allowing us to work with data dynamically.
// Variable names should be unique and descriptive to help users understand the purpose of the code.

int main()
{

    int age = 25;
    float Pi = 3.14;
    char ch = 'A';
    string str = "Today";
    double D = 155456;
    bool Bl = true;

    cout << age << endl;
    cout << Pi << endl;
    cout << ch << endl;
    cout << str << endl;
    cout << D << endl;
    cout << Bl << endl;

    return 0;
}